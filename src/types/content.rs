/*
now_ms() — current time as Unix milliseconds (ms since Jan 1, 1970 UTC).

RUST QUIRK: Rust's time API is intentionally explicit about fallibility.
`duration_since(UNIX_EPOCH)` returns a Result<Duration, SystemTimeError>
because in theory the system clock could be set before the epoch (negative).
`.unwrap_or_default()` handles this gracefully: if it fails, use Duration::ZERO
(timestamp = 0) rather than panicking. Panicking in a timestamp helper would
be a terrible user experience.

Chain breakdown:
  SystemTime::now()              → current system time (platform-specific)
  .duration_since(UNIX_EPOCH)   → Result<Duration, SystemTimeError>
  .unwrap_or_default()           → Duration (0 on error)
  .as_millis()                   → u128 (milliseconds as 128-bit integer)
  as u64                         → truncate to 64-bit (safe until year 584,542,046)

Python analogy:
  import time
  now_ms = int(time.time() * 1000)
*/
pub fn now_ms() -> u64 {
    std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .unwrap_or_default()
        .as_millis() as u64
}
