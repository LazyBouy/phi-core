/* StopReason enum captures why an assistant message generation stopped. This is crucial for understanding the LLM's behavior and for debugging.
LLMS vary in how they signal the end of generation — some use explicit stop reasons, others return errors for context overflow, etc.
By standardizing these into a StopReason enum, we can handle them uniformly in the agent loop and UI.
However, providers differ in how they indicate context overflow — some return a StopReason::Error with a specific error message,
while others might have a dedicated stop reason.
Therefore, we also have provider specific implmentation and he is_context_overflow() method on Message to check for overflow conditions in
a provider-agnostic way, by inspecting the error_message field when stop_reason is Error.
*/

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "camelCase")]
pub enum StopReason {
    Stop,
    Length, //MaxTokens, TokenLimit, ContextOverflow — different providers use different terms for the same underlying reason: the generation
    ToolUse,
    Error,
    Aborted,
    /* Below are the options adopted from other Agentic models and our own experience with LLM APIs.*/
    MaxTurns, // Adopted from: LangGraph, Autogen. Description: Indicates generation stopped because the conversation reached the maximum allowed turns.
    UserStop, // Adopted from: AutoGPT. Description: Indicates generation was stopped by an explicit user command (e.g., "stop", "cancel").
    Handoff, // OpenAI Swarm, Adopted from: AutoGPT. Description: Indicates the agent is handing off to a human operator (e.g., for approval or intervention).
    GuardRail, // Adopted from: Llama Guard, Bedrock. Description: Indicates generation was stopped by an internal guardrail (e.g., content moderation, safety filter) - — different from Aborted
    ContextCompacted, // Adopted from: Anthropic's context compaction feature. Description: Indicates generation stopped because the model compacted the context to fit within limits, which may have caused loss of information.
    Paused, // Adopted from: LangGraph. Description: Indicates generation was paused (e.g., waiting for external input or async operation) rather than fully stopped.
}

//*********** STOP REASON HELPERS ***********
/*
fmt::Display — Rust's "how to print this type as a human-readable string"

Python analogy: implementing __str__(self) on a class.

In Rust, you use fmt::Display instead of __str__. Once implemented, you can:
  - println!("{}", stop_reason)          ← uses Display
  - format!("Stopped: {}", stop_reason) ← uses Display
  - stop_reason.to_string()              ← auto-generated via Display!

The signature:
  fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result

  &self           — borrow the value (read-only)
  f               — the "output buffer" you write into
  fmt::Result     — Ok(()) if write succeeded, Err if not (rare)
  fmt::Formatter<'_> — '_ is an elided (compiler-inferred) lifetime for the borrow

The write!() macro writes into f (the formatter buffer):
  write!(f, "stop")  ←→  Python's  return "stop"

We implement this manually (instead of deriving) because we want custom
camelCase strings ("toolUse", not "ToolUse") that match the wire protocol.
*/
impl fmt::Display for StopReason {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Stop => write!(f, "stop"),
            Self::Length => write!(f, "length"),
            Self::ToolUse => write!(f, "toolUse"),
            Self::Error => write!(f, "error"),
            Self::Aborted => write!(f, "aborted"),
            Self::MaxTurns => write!(f, "maxTurns"),
            Self::UserStop => write!(f, "userStop"),
            Self::Handoff => write!(f, "handoff"),
            Self::GuardRail => write!(f, "guardRail"),
            Self::ContextCompacted => write!(f, "contextCompacted"),
            Self::Paused => write!(f, "paused"),
        }
    }
}

